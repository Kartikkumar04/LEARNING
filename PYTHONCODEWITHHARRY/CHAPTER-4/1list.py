# In Lists we can store any type of data.

a = ["Apple", "Orange", 5, 345.06, False, "Aakash", "Rohan"]
print(a,a[2])

# Lists are mutable meaning we can change their values.

b = ["CODE", "DEVELOP","ER","04"]
b[2] = "ERS"
print(b)

# In lists we can also perform slicing like strings

print(a[1:4])   